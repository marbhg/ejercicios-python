#Creamos la frase en mayuscyla 
fraseMayuscula = frase.upper()

#creamos la frase en minuscula 
fraseMinuscula = frase.lower()

#Imprimimos el resultado 
print("Longitud de la frase ", longitud)
print("Mostramos la frase en mayuscula: ", fraseMayuscula)
print("Mostramos la frase en minuscula: ", fraseMinuscula)





